
# You can use chaining to replace the 'and' operator
age = int(input("How old are you? "))

print(18 < age and age <=65)
# This is equivalent to:
print(18 < age <= 65)

# Chaining task example
height = float(input("How tall are you(cm)? "))
weight = float(input("How much do you weigh(Kg)? "))

print(131 < height < 190)
print(50 < weight < 120)

