# Operator precedence
print("10 + 5 * 2 =", 10+5*2)

print("10 - 5 * 10 + 5 =", 10-5*10+5)

print("5 * 10 ** 2 =", 5*10**2)

print("(10 + 5) * 2 =", (10+5)*2)

print("10 - 5 * (10 + 5) =", 10-5*(10+5))

print("(5 * 10) ** 2 =", (5*10)**2)

print("12 + (5 * 2 + 3) =", 12+(5*2+3))

print("12 + (5 * (2 + 3)) =", 12+(5*(2+3)))