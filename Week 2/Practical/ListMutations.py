
# Slicing to insert two new names into the list
names = ["Terry", "John", "Michael", "Eric", "Terry", "Graham"]
print(names)

names[-1:-1] = "Todd", "Tucker"
print(names)

# Appending a value to the end of the list
names.append("Tommy")
print(names)

# Using the string addition method
names += ["Tony"]
print(names)
